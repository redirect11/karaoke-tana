// Minimal service worker required for Web Notifications on Chrome Android.
// Chrome Android does not support new Notification() from page context;
// notifications must be shown via ServiceWorkerRegistration.showNotification().
self.addEventListener('notificationclick', function (event) {
  event.notification.close();
  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then(function (clientList) {
      for (const client of clientList) {
        if ('focus' in client) return client.focus();
      }
      if (clients.openWindow) return clients.openWindow('./');
    })
  );
});
